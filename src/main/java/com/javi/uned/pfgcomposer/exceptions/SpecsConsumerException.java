package com.javi.uned.pfgcomposer.exceptions;

public class SpecsConsumerException extends Exception {

    public SpecsConsumerException() {
    }

    public SpecsConsumerException(String message) {
        super(message);
    }

    public SpecsConsumerException(String message, Throwable cause) {
        super(message, cause);
    }

    public SpecsConsumerException(Throwable cause) {
        super(cause);
    }

}
